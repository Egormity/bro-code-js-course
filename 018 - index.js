const PI = 3.14;



if(PI == "3.14"){
    console.log(`It is Pi`);
}
else{
    console.log(`It is not Pi`);
}



if(PI === "3.14"){
    console.log(`It is Pi`);
}
else{
    console.log(`It is not Pi`);
}



if(PI != "3.14"){
    console.log(`It is not Pi`);
}
else{
    console.log(`It is Pi`);
}



if(PI !== "3.14"){
    console.log(`It is not Pi`);
}
else{
    console.log(`It is Pi`);
}